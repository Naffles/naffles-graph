import {
  DiamondCut as DiamondCutEvent,
  OwnershipTransferred as OwnershipTransferredEvent,
  RoleAdminChanged as RoleAdminChangedEvent,
  RoleGranted as RoleGrantedEvent,
  RoleRevoked as RoleRevokedEvent
} from "../generated/L2OpenEntryTicketDiamond/L2OpenEntryTicketDiamond"
import {
  DiamondCut,
  OwnershipTransferred,
  RoleAdminChanged,
  RoleGranted,
  RoleRevoked
} from "../generated/schema"

export function handleDiamondCut(event: DiamondCutEvent): void {
  let entity = new DiamondCut(
    event.transaction.hash.concatI32(event.logIndex.toI32())
  )
  entity.facetCuts = event.params.facetCuts
  entity.target = event.params.target
  entity.data = event.params.data

  entity.blockNumber = event.block.number
  entity.blockTimestamp = event.block.timestamp
  entity.transactionHash = event.transaction.hash

  entity.save()
}

